            <p id="header">OPISKELIJABUDJETIN VARASSA</p>

            <p id="first">Päiväkodista asti meidät on lautasmallin ja ruokapyramidin avulla opetettu syömään oikein. Korkeakoulussa nämä opit asettuvat helposti haasteeksi. Miten
              opiskelijabudjetilla selviytydytään tästä haasteesta?
            </p>

            <p id="author">Teksti ja kuvat: Saija-Riina Harju
            </p>

            <img id="image1" src="opiskelijabudjetti3_web.png" alt="breakfast">

            <p>Kela maksoi opintotukea lukuvuonna 2016-2017 lähes  280 300 opiskelijalle. Lukuvuoden lopussa opintovelallisia oli noin 377 900 ja heillä oli lainaa keskimäärin 6 691 euroa. Itsenäisesti asuvan 18 vuotta täyttäneen helsinkiläisopiskelijan suurin mahdollinen opintoraha on 250 euroa ja asumistuki 406 euroa kuukaudessa. Mikäli opiskelijalla ei ole muita tuloja, opintotuki on parhaimmillaan 656 euroa. Hyvällä tuurilla jäljelle  jää vuokranmaksun jälkeen sata euroa. Tästä vähennetään pakolliset laskut, kuten vesi, sähkö ja puhelin, jonka jälkeen voidaan miettiä ruokaa. Aiheuttaa usein ihmetystä, miksi työttömän on laskettu tarvitsevan jokapäivisiin menoihinsa 491 euroa, mutta opiskelija pärjää selkeästi vähemmällä. Kysyin opiskelijoiden kokemuksia opiskelijabudjetilla elämisestä selvittääkseni heidän ruokatottumuksiaan.
            </p>

            <p>”Mulla menee 150-200 euroa kuukaudessa ruokaan”, kertoo Inkeri, vegaani opiskelija. Kaikkiruokainen opiskelija Sarianne on samoilla linjoilla. Hän käyttää ruokaan noin 160 euroa kuukaudessa.
              ”Ruokaan kuluu rahaa noin 200 euroa kuussa. Jos opiskelijaravintolan ruoka olis ravitsevampaa, ei tarviis ostaa kotiin niin paljon ruokaa”, pohtii kaikkiruokainen opiskelija Anni ja kertoo, ettei opintotuella tule toimeen vaan on nostettava lainaa ja käytävä töissä. ”Elän opintolainalla”, toteaa myös Sarianne. Pääosin kasvisruokaa syövä Jarkko kertoo tehneensä suurimman osan opiskeluajastaan töitä ja tulee nyt säästöillä toimeen.
            </p>

            <img id="image1" src="opiskelijabudjetti2_web.png" alt="tomato">
            <p>Suosituksen mukainen ruokavalio on monipuolinen ja vaihteleva. Se koostuu useista eri raaka-aineista ja sisältää sopivassa suhteessa tärkeimpiä ravintoaineitamme; hiilihydraatteja, rasvoja ja proteiineja. Päivittäin on hyvä syödä säännöllisin väliajoin: aamupala, lounas, päivällinen ja iltapala sekä tarvittaessa kaksi välipalaa.
            </p>

            <p>Hintaa miettiessä päätyy kuitenkin helposti tekemään päivästä toiseen samaa, yksipuolista ruokaa.
              ”Kun ostan ruokaa, mä ajattelen, että siihen saa mennä rahaa. Jos olisi enemmän rahaa niin kyllä siihen saisi menemään enemmänkin. Ostaisin varmaan useammin salaattia. Tuntuu ettei oo mitään järkeä ostaa jotain kolmen euron salaattipussia, kun samalla hinnalla saa kolme purkkia papuja, jotka riittää kolmeksi päiväksi”, pohtii Inkeri. Sarianne kokee pystyvänsä syömään terveellisesti ja monipuolisesti: ”Loppupeleissä terveellinen ruoka on kuitenkin halvempaa kuin einesruoka.”
            </p>

            <p>Pienet 10 sentin säästöt muodostuvat nopeasti selväksi säästöksi. ”Jokaikinen sentti pitää laskea, jos haluaa syödä kunnolla”, Anni sanoo. Myös Inkeri kertoo laskevansa ostoksensa yhteen kauppareissuillaan ja korostaa ruokavalintoja: ”Siinäkin säästää, kun syö vegaanisesti, koska esimerkiksi soijarouhepussi maksaa 3-4 euroa. Se kestää kauan, jos vertaa vaikka kanapakettiin.” Annilla on vinkata kauppojen ilta-alennuksista: ”Prismoihin ja Minimaneihin voi mennä tuntia ennen sulkemisaikaa niin saa halvemmalla. Ainoa mahdollisuus ostaa kalaa, joka on muuten niin kallista.”
            </p>

            <p id="main">“ Jokaikinen sentti pitää laskea, jos haluaa syödä kunnolla.”
            </p>

            <p>Luovuus palkitaan tässäkin asiassa. ”Jos ei oo varaa juustoon ja voihin, niin voi ostaa sulatejuustoo”, vinkkaa Sarianne ja jatkaa: ”Banaanit on halpoja sekä täyttäviä ja niistä voi tehdä myös jäätelöä.” Toisaalta makaroni, jauheliha ja kananmunat ovat helppo ja halpa ratkaisu. “Heittää vaan lautaselle kaiken mitä löytyy; makaronia, ketsuppia ja munakas. Siitä saa protskuu ja ruisleipä kylkeen”, Sarianne luettelee. Anni kertoo jauhelihan ja makaronin olevan todella tiukan kuukauden ruoka. Myös Inkeri jakaa budjettiruokavinkkinsä: ”Paketti kidneypapuja, valmis pastakastike ja paketti spagettia tai makaronia. Maksaa alle kolme euroa ja siitä syö kaks päivää.”
            </p>

              <figure>
                <img id="image2" src="opiskelijabudjetti1_webpysty.png" alt="food">
              </figure>

            <p>Ravintosuositusten mukaan kalaa tulisi syödä 2-3 kertaa viikossa. Lisäksi päivittäin pitäisi syödä puoli kiloa hedelmiä ja kasviksia, välttää punaista lihaa ja kovia rasvoja. Lautasmalli kuvaa, mistä perusaineksista suositusten mukainen ruokavalio koostuu. Puolet lautasesta pitäisi täyttyä kasviksista. Neljännes lautasesta tulisi olla perunaa, pastaa tai riisiä. Viimeinen neljännes jää kala-, liha- tai munaruoalle, jonka voi korvata palkokasveja, pähkinöitä tai siemeniä sisältävällä kasvisruoalla. Ruokajuomaksi rasvatonta maitoa, piimää tai vettä. Lisäksi täysjyväleipää, jonka päällä on sipaisu pehmeää kasvirasvalevitettä. Jälkiruoaksi marjoja tai hedelmiä.
            </p>

            <p>Jokaisella on silti vapaus valita mitä syö ja ruokasuositukset soveltuvat vain ryhmätasolla. Niihin vaikuttavat aina maiden omat ruokakulttuurit ja mitä ravintoaineen lähteitä on käytössä maan ruokavarannossa. Terveellisellä ruokavaliolla pyritään useimmiten hyvään oloon. Anni pitää ruokavaliota tärkeänä asiana jaksamisen kannalta: ”Mä tykkään syödä kunnolla, jotta energiatasot pysyy hyvänä ja jaksaa tehdä asioita.” Myös Jarkko pitää oleellisena asiana ruokavalion vaikutusta omaan olotilaan: ”Koen oloni energisemmäksi ja aineenvaihduntani on parantunut lihan poisjättämisen jälkeen. Mielestäni ravitsemussuosituksia pitäisi päivittää monilta osin. Ravintosuosituksia tärkeämpää on oman kehon kuuntelu ja se, kuinka energiseksi tunnet olosi.” Kasvisruokavaliota pidetään usein kalliina, mutta haastattelemani vegaaniopiskelijat eivät koe ongelmaksi elää vegaanisesti pienellä budjetilla.
            </p>

            <p>Haastattelemani opiskelijat vaikuttavat toimeentulevilta ja melko tyytyväisiltä omaan tilanteeseensa, vaikka toimeentulon ja opiskeluelämän sovittaminen on välillä luovuuden ja budjetoinnin takana. Opintotuen hyvät ja huonot puolet, sekä omien valintojen merkitys ymmärretään. ”Onhan se toisaalta hyvä, että opiskelijana saa tukia eikä välttämättä tarvitse käydä edes töissä”, toteaa Inkeri. Pääasiassa opiskelijat kertovat tinkivänsä ennemmin jostain muusta, kuin ruoasta. Opiskelijabudjetilla eläminen vaatii valintojen tekemistä ja vapaa-ajan viettämiseen jää helposti heikosti rahaa. ”Koen, että opintotuki on kohtuuttoman pieni, mutta useilla opiskelijoilla on myös peiliin katsomisen paikka mihin he rahansa käyttävät”, pohtii Jarkko.
            </p>

            <p>Opiskeluaika on tasapainoilua opiskelun, lainanottamisen, työnteon ja vapaa-ajan kanssa. Edes kohtuullisen ruokavalion saavuttaakseen, opiskelijan on käytävä myös töissä tai nostettava lainaa. Jostain on aina tingittävä ja valinta on jokaisen oma. Toinen saattaa voida hyvin syödessään vain kouluruoan ja iltapalan, kun toinen vaatii useamman ravinteikkaan aterian tunteakseen itsensä energiseksi ja hyvinvoivaksi.
            </p>

