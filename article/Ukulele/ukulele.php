            <h2>Inspiration and motivation</h2>

            <p>There are many websites as well as YouTube channels which explain very well how to play such or such tracks and which also gives you some advice and habit to have. Here are some of them I started on: Ukulele teacher (YouTube channel), Ukulele cheats (YouTube channel), Michael Lynch (YouTube channel), ukuleletricks.com and ukulelehunt.com.</p>

            <p>As time goes by, with experience you will progress quickly but there will always be a given moment when you will have the impression of stagnating. For me it was when you had to make crossed chords, no panic, it's normal, you just have to be patient and keep your motivation. Watch videos of the music you dream of learning in the future, it will motivate you more than anything.
			       </p>

