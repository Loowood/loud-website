            <p id="header">A DIG INTO GAME DEVELOPMENT</p>

            <p id="first">Nowadays, a lot of video games are created by big companies. Though, there is an underground network of independant creators, who unite themselves into creating plenty of original little gems. Let's have a look at them, shall we ?</p>

            <p id="author">Text: Loris GIRAUD. Art: Open Pixel Project
            </p>

