			<p id="main">THE COVER OF THE MINE
            </p>
			
            <p>
            In 2017, 364 million people assisted to the Worlds Championship of League of Legends at Pekin, in China. The same year, Pokemon Go blew up everyone with peaks at 25 million daily players. During the 100 first days of Fortnite, half a trillion shots were fired with now up to 20 million of players from all around the globe. Twitch, a live streaming website, mainly based on video games, has more viewers than HBO and Netflix combined. In January 2018, the website had even beat CNN.
			The e-sport scene has recently burst in, with an European market estimated at over 300 million of American dollars. Video games are gaining more and more users and spectators, with new games produced each month. The Nintendo announcement for the fifth game in the Super Smash Brothers Series Behind this gigantic window of well-known games, lies plenty of original, sometimes strange gems known as “indie games”.
            </p>

            <p id="main">THE INDEPENDENT VEIN
            </p>

            <p>The word “indie” is wide though, and not only limited to games. In fact, it describes anything that is being produced underground, outside of the traditional commercial way. Therefore, every game created without the help of any publisher can be considered as one. Even though, there are still games in the middle, that could be called “indie” while being supported by a publisher. Mainly because they have the spirit of them, as players find they were created from passionate creators. It’s the case of Ori and the Blind Forest. A gorgeous 2D game created by Moon Studios, a small team which is nevertheless supported by Microsoft. 
            </p>

              <figure>
                <img id="image2" src="spr_m_traveler_walk_anim.gif" alt="A pixelated animation">
