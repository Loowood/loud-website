        <h2>Mikä inspiroi näyttelijäntyössä?</h2>

        <figure>

        </figure>
        <p><strong>Pere:</strong> Toiset ihmiset, kollegat, kanssanäyttelijät, ohjaaja ja koko muu projekti työryhmä. Se on tosi inspiroivaa. Tarinat ja tekstit myös inspiroi. </p>
        <p><strong> Johansson:</strong> Uuden tekeminen ja uuden kohtaaminen. Tylsiä työpäiviä ei ole. Aina ollaan jonkun uuden äärellä. </p>
        <p><strong> Vuorela:</strong> Kokonaisuus inspiroi. Olen ollut Seinäjoella 15 vuotta ja olen saanut tehdä valtavan määrän rooleja. Työyhteisöt ja erilaiset roolit inspiroivat. Koen tämän edelleen omaksi haaveammatikseni ja saan toteuttaa itseäni monella eri tavalla. </p>
        <h2>Miten pyrit kehittämään itseäsi näyttelijänä?</h2>
        <p><strong>Johansson:</strong> Vois kehittyä paljon enemmänkin. Näyttelijä kehittyy kokoajan, mutta kyllä sitä jonkinlaisessa kunnossa pitää olla. Sellasta mä teen paljon, että mä tarkkailen ihmisiä. Pitää olla jonkinlaista yleissivistystä ja ymmärrystä kaikista asioista, ettei vedä itseään ihan rappiolle. </p>
        <p><strong> Vuorela:</strong> Ensinnäkin omasta fyysisestä kunnosta huolehtiminen on tärkeää jaksamisen kannalta ja se, että on omassa itsessään hyvä olla. Se on kaiken a ja o. Sama pätee kyllä kaikkiin ammatteihin, mutta erityisesti teatteri alalla. Omasta fyysisestä ja henkisestä kunnosta on pidettävä hyvää huolta. Aina kun tulee uusi rooli vastaan, pyrin ottamaan siitä kaiken irti. </p>
        <h2>Syntyykö työyhteisössä konflikteja?</h2>
        <p><strong>Pere:</strong> Se tietysti riippuu työyhteisöistä ja tilanteista, mutta tottakai. Ehkä suurin konfliktitilanteen aiheuttaja on henkilöiden keskenäisen ymmärryksen puute. </p>
        <p><strong> Johansson:</strong> Kyllähän joskus jokaisessa työyhteisössä syntyy konflikteja, mutta usein ne menee ohi ihan itsestään. Yleensä isommat konfliktit ovat näyttelijän ja ohjaajan välisiä näkemyseroja. </p>
        <p id="main">“Kun täältä Seinäjoelta katsoo suomen teatterikenttää niin asenne on muuttumassa ja muuttunut ja se vaikuttaa meidän tulevaan teatterikenttään.”
        </p>

        <h2>Onko teatterialalla ongelmia?</h2>

        <p>Lokakuussa 2017 mailmanlaajuiseksi ilmiöksi sosiaalisessa mediassa noussut Me Too -kampanja on ollut keino ihmisille jakaa kokemuksiaan seksuaalisesta häirinnästä, joka kehittää tietoisuutta ja vastustusta seksuaaliselle häirinnälle. Kampanja on herättänyt paljon keskustelua etenkin taide- ja kulttuurialalla. Voimakkaimmin kampanjan vaikutukset ovat näkyneet Yhdysvalloissa, mutat myös suomessa #metoo on saanut merkittävää huomiota.
        </p>

        <p>
          Keskustelua on myös herättänyt teatteriorganisaatiota koskevat asiat. Teattereiden työntekijöillä sekä teattereiden tekniikasta vastaavilla työntekijöillä on muutamia esimiesportaita, mutta samaa ei ole taiteellisella henkilökunnalla. Taiteellisen henkilökunnan lähin esimies on usein teatteri johtaja, joka voi usein olla etäisempi, kun toivotaan. Usein näyttelijät ovat erillään eri tuotannoissa ja esimies ei välttämättä ole mukana koko tuotannossa.
        </p>
        <p><strong> Vuorela:</strong> #metoo on tietysti edelleen asia jonka kanssa teatteriliitto päivittäin käy keskustelua, joista pitää keskustella. Sitä ei voi unohtaa ja on hyvä asia, että se on tullut esille. </p>
        <p><strong> Pere:</strong> Tää aikakausi on sellanen, että mahdollisista ongelmakohdista, esimerkiksi tasaarvottumuudesta tai häiriökäyttäytymisestä on alettu puhumaan. </p>
        <h2>Onko teatteriala muuttunut ja miten se on vaikuttanut omaan työhösi näyttelijänä?</h2>
        <p><strong>Johansson:</strong> Sanoisin, että noin 20 vuoden työkokemuksen aikana se ei ole kauheasti muuttunut. Viimeaikoina ehkä kiireen ja työn määrä on lisääntynyt ja näyttelijäliiton tekemät tilastot sen osoittaa. Vierailevien ja kiinnitettyjen näyttelijöiden määrä on laskenut, mutta ensi-iltojen määrä on kasvanut, mikä tarkoittaa että pienemmällä porukalla tehdään enemmän. </p>
        <p><strong> Vuorela:</strong> Kun täältä seinäjoelta katsoo suomen teatterikenttää, niin asenne on muuttumassa ja muuttunut ja se vaikuttaa meidän tulevaan teatterikenttään. Kuinka paljon meillä on tulevaisuudessa tällaisia laitoksia ja minkälaisia työntekijöitä siellä on. Suomessa on todella vahva harrastajakenttä, että valtaako harrastajat nämä teatterit vai saadaanko me tänne ammattilaisia lisää. Asennemuutos on aika voimakas. Samalla monilla on hyvin voimakkatita mielipiteitä siitä mihin, suuntaan ollaan menossa. Se on milestäny hyvä asia, mutta tällaien rakenteiden muuttaminen on todella hidasta. </p>
