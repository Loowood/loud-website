			<h2>Ready, Steady, Go!</h2>
            <p>If you like to get dirty but you don’t
			like football you can try the Wife Carrying! 
			This "discipline" dates back 
			to the 19th century if we believe the legend.
			A local bandit named Ronkainen used to rob women 
			and bring them home by carrying them on his back.
			One act replicates each year since 1990 by the couples
			competing at the world championships organized in Sonkajärvi
			in ​​Eastern Finland.
			</p>
			
			<p>The legend of this brigand has been honored since 1990
			at the World Women's Wear Championship. Participating men
			must make a special run with a woman on their back. 
			The official circuit, which stretches 253.5 meters, 
			takes them into the water, sand, grass and gravel.
			Different obstacles also stand in their way. 
			The winner is of course the fastest couple.
			</p>
			
			<p>As a man, it is obviously necessary to have a woman
			to carry to participate in the competition of wife carrying .
			This woman does not necessary have to be the wife or companion 
			of the man. Her minimum weight must reach 49 kilos. 
			When her weight is lower, she must wear a backpack
			to reach 49 kilos. When the woman falls off his back,
			the couple must take 15 seconds penalty.
            </p>

            <h2>Throw it away! </h2>

            <p> Don’t know what to do with your old Nokia? 
			We have a little idea for you, show him who is the
			boss and try to throw it to the moon! 
			This is the only sport where you can throw 
			all the frustrations caused by these modern equipments away.
			</p>
			
			<p>The mobile phone throwing started in Savonlinna 
			in Finland. The company Fennolingua was in partnership
			with local recycling centers and they created a championship 
			tournament.
			</p>
			
			<p>The championship participants compete in four main categories:
			the over-the-shoulder throw where you have to throw it as far as
			you can; the "freestyle" category where everything is a matter of
			aesthetics and creativity and the team category, where three
			teammates have one throw each and their scores are added together.
			Finally, there is the junior category, reserved for 
			kids under 12 years. 
			Beyond the bizarre sport, the organizers insist on the
			environmental vocation of the competition, 
			all phones are recycled at the end of the competition. 
            </p>
			<h2>Just try it!</h2>
			<p>These sports gain a lot of popularity and now many 
			countries organize their own championships.
			The global interest turned it into an international sport! 
			If you are in Finland, try to attend or participate, 
			it will be a good story to tell to your friends !
			</p>

