            <p>Finns are special people with strange tastes.
			They enjoy salty liquorice after all.
			At first glance, they can be a little cold and serious,
			but you will discover that they have an excellent sense of humor.
			Take a look at the unusual sporting events that draw thousands
			of spectators and contestants every year! Here’s a round-up of
			a few. 
            </p>

            <h2>Go on the mud ! 
            </h2>

            <p>The top Finnish cross-country skiers were
			training in the mud in summer to be in perfect condition 
			on their cross-country skis once winter came. They thought
			it would be more fun to associate these painful training methods
			with the most universally shared fun activity, football.
			They organize the first football mud championship in 1998
			in Hyrynsalmi. The Swamp Soccer was born.</p>

			<p>Christiano Ronaldo’s technical subtleties will not be
			of much use here. The field is a swamp, there are no offside 
			rules but as usual, the one with the most goals wins. Okay,
			now you're ready for the World Football Championships in the
			swamps of Hyrynsalmi in northeastern Finland. Be careful not 
			to lose your boots in the mud... 
            </p>

