            <p id="header">In pursuit of happiness</p>

            <p id="first">Everyone would agree that being happy is anyone's ultimate goal. If this is fundamentally the only goal in one's life, why is it that so many people can't achieve it? Why is happiness so hard to gain and especially hard to maintain?
            </p>

            <p id="author">Text and photo: Max Hoving Illustration: Hilda Anttila
            </p>

            <img id="image1" src="trees.jpg" alt="trees">
  
            <p>Why are things hard? Many things can weigh us down and it may sometimes seem that things are unnecessarily hard. If we are such smart creatures, why is it still so hard to get what we want? One reason why life is difficult is that people are competitive. We always compare ourselves to others and want to become better than them. We tend to only look at our shortcomings and feel like we failed at life if we are not the best we could possibly be. Competitiveness also means that we don?t always get along with others because we all have our own interests and can?t possibly live for everyone.
            </p>

            <h2>Simpler times</h2>
		
            <p>Back in the stone-age, people didn't really have time to think about happiness. The thrive to tell us that we needed to do to "succeed at life" came from our instincts that told us we needed to live to reproduce. Since then, society has advanced, making the collection of food less of an issue and reproducing less important. Because of our primal instincts to "desire", more arbitrary things have been created for us to worry over: politics, social acceptance and of course; money.
            </p>

            <h2>Does money buy happiness?</h2>

            <p>Stories about the effect of money on one's happiness have been around for a long time, for example  a Christmas Carol or The Great Gatsby. "Money doesn't buy happiness" is a response often stated in these stories. However, many studies show that there is in fact a positive correlation between income and happiness. It is pretty unjustifiable to believe poor people have it just as good or even better than rich people. However, money does not guarantee happiness. The reason that money is not always the answer is because we adapt to comfort. As we grow and accustomed to living a more comfortable life, we can get blinded by what we are still missing. We only miss the extra comfort once it's gone. So, does money buy happiness or not? Relatively speaking, yes, but it turns out that the result could be heavily influenced by how it is spent. A study published in 2008 shows that the positive correlation between money and happiness may be the result of how much people had to spend on others. The more mone               y one has, the more generous one could be towards the less fortunate, therefore raising their satisfaction. Rich people spending no money on others tend to end up less happy then poor people sharing whatever little income they have. 
            </p>

            <p id="main">"Luck is what happens when preperation meets opportunity"
            </p>

            <h2>Luck and mentality</h2>

            <p>So if money is not a guaranteed answer to attaining happiness, what is? The science of happiness is such a difficult one that it may seem it all comes down to dumb luck. Some were blessed with confidence, others with insecurity. Some are being paid for doing nothing, others have to struggle for every penny they earn. Many things can weigh us down and it may sometimes seem that things are unnecessarily unfair. So should we just stop trying all together to become happy if it all comes down to fate? As Roman philosopher Seneca once said: "luck is what happens when preparation meets opportunity" In other words: Being open to chances and working hard to increase those chances is what makes the difference between "lucky" people and "unlucky" people. One's mentality is a big factor in being happy, as it determines one's ability to motivate themselves to stay positive and to seek out opportunities. The answer we often seek in results is not what we truly want because in the end, the feeling of self-wor               that determines how successful we feel at life. Life may be hard and unfair, but nothing worth having comes easy.
            </p>

