            </p>
			  <h2>IMPORTANT:
Before you touch the motherboard, you must wear an antistatic wristband and attach the other end to something grounded. Keep your motherboard also on something grounded, you can put it on your motherboards box. Static electricity from your body might damage your motherboard. Read all of your manuals in order to instal your parts correctly! Especially the processor.
</h2>
			  <h2>Building</h2>
			  <p>Firstly I installed my RAM-stick, processor and SSD. Before I moved my motherboard in the case, I attached all of my support-screws inside. Installing the motherboard was bit tricky since you must attach it to the support-screws and I/O shield at the same time but once I got it screwed still I could safely continue. Before I can attach any other big components I had to instal the PSU on the case and attach all the wires I had on the case fans and the motherboard because once the bigger components are installed, connections are really hard to get access to. Read your manuals before attaching any wires. When I was ready to instal the cooler I faced an unexpected problem; my radiator was 0,5 cm too big to attach it on my front fans. I could have easily removed my fans and move them but my lower fan wasn’t screwed like the other two. It was bolted in the case and the bolts prevented me from installing my radiator. I had to get a smaller radiator.
When I got my new cooler and installed it on my back fan, I could continue my built. Graphics card and the router were easy to instal, like legos. After that I was DONE. And I booted my machine. My machine booted, but the screen did not find a singall. Not when using HDMI-  or VGA-cable. It turns out, that I needed a DVI-cable to connect my screen to my PC. It worked. My PC is running fine now. It had few issues with rebooting but I got it fixed when I tweaked some of my connection-pins. 
In the end, now I have a great machine for editing witch I can upgrade piece by piece every two to three years when I need to. This was a really rewarding project that I was planning to do some time and I’m glad that I did it. In this project I’ve learned more about computers that I’ve thought I needed to know and I will continue learning when I’m going to upgrade my PC next time.

            </p>
