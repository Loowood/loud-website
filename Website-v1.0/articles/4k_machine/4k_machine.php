            <p>For some years I had been editing my videos on my old laptop but now it had became too slow for doing any work. I need to buy a new computer for serious editing work. I have been interested on custom computer builds for a while now. I have been following few channels on youtube who built, discuss and preview computers and their components for six months now. If I don’t have a computer of my own, I’m a carpenter without tools. I looked up my savings and decided my budget on 2000€ (2K). It was time for some research.

            </p>

            
<h2>Research</h2>
            <p>Since I was building my computer primarily for editing, I needed to build around my editing software of choice, Adobe Premiere and After Effects. They both require minimum of 8 Gb of RAM-memory to work (16 Gb is recommended).  I got one 16 Gb RAM-tick. I also found out that Graphics Card needs at least 8 Gb of memory to process 4K-video. When editing 4K-video you can lower your program monitors quality or use proxies to ease your workflow but you need that 8 Gb to render your video.
One of the employees of my  local electronic retail store helped me for choosing my processor. He told me that most important aspect of processors are cores. Processors speeds are determined on their frequencys but if processor has multiple cores it can make multiple calculations same time. Intel processors tend to have faster cores, but AMD processors tend to have more cores on their processors. 
            </p>

              <figure>
                <img id="image2" src="editing2.png" alt="poisson">
              </figure>

            <p>That’s why I chose AMD:s Ryzen 5 1600x as my processor. Motherboards has more differences than just size and witch processor they support. They also have different ports.
One important thing to figure out is your coolers size because fan cooler can take a lot of space. You must have a big enough case to fit your cooler in. Also if you’re using a water cooler you must know if your case supports coolers radiator. There are different sizes of radiators you can instal, from 120 mm to 380 mm.
I also considered using only hard drives for my file and software strorage since they are cheap. But a friend recomended that I should buy small (256 Gb) SSD where I instal my Operating System and important programs. Because SSD:s are much faster than hard drives I can open my programs much faster than from a hard drive. 

            </p>
<p id="main"> Processors speeds are determined on their frequencys but if processor has multiple cores it can make multiple calculations same time.
            </p>
            <h2>Mandatory components</h2>

            <p>Motherboard is a giant circuitboard where you instal all the other components. Motherboards have different amount of slots for multiple components and can support limited amount of certain components (read your motherboards package and manual for best info). Motherboards also have different sizes. Most common sizes are ATX, micro-ATX and mini-ATX. They also have ports for your monitors, mics, speakers and your USB-devices and they are protected by  I/O shield. CPU, (Central Processing Unit) also known as
Processor is the brain of your computer. Intel and AMD are the most common processor manufacturers. When choosing your processor keep in mind, that there are different motherboards for Intel and AMD processors. Your processor need a cooler to keep it working. There are two types of coolers; cheap fan coolers that may take room from your RAM slots and more expensive water coolers, that deeds a radiator installed on your case fans to work. 
RAM-sticks stores data and machine code that’s currently being used. Many softwares require certain amount of RAM in order to work properly.
GPU (Graphics Processing Unit) also knowns as Graphics Card is the most expensive component of your built. It accelerates creation of images on your computer. Modern games and complicated editing processes need lot of power on Graphics Card in order to work properly. Power Supply unit is a box that gives power to your computer. Case is where you instal your components and hard drive and/or SSD(Solid State Drive) is where you can store your programs and files. Difference in traditional hard drive and SSD is their writing and reading speed. SSD performs much faster than hard drive but is also more expensive, slower hard drives are cheap as dirt.

            </p>
			  <h2>Buying components</h2>
<p>I bought most of my components from my local electronic retail stores. Only problem I had on buying components was with the graphics card. Ever since Bitcoin and other cryptocurrencys became popular, people started buying graphics cards to mine coins. It was really hard to get a good graphics card in whole of Finland. Importers had to delay lot of shipments in many stores because of their demande. This also raised their prices a lot. Hopefully when you read this prices have fallen and demand for crypto mining has fallen. In the end I managed to buy all of my components under the budget.
