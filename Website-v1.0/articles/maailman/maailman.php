            <p id="first">Kyrö Distillery Companyn suosio ei vaikuta hiipuvan, vaan ruisviskiä valmistavan tislaamon tunnettavuus sen sijaan jatkaa kasvuaan.
            </p>

            <p id="author">Text: Sarianne Manninen Pictures: Tomi Salminen
            </p>

            <img id="image1" src="maailman1.jpg" alt="ponton">
            <figcaption>Kyrö Distilleryn lanseeraama Napue Gin, jonka avulla yritys nousi maailmankartalle</figcaption>

            <p>Kyrö Distillery Company on vuonna 2012 Isossakyrössä perustettu tislaamo, joka aloitti tislaamisen vuonna 2014. Erityiseksi tislaamon tekee se, että yritys valmistaa tuotteita rukiista. Rukiiset juomat saavat lisämakua lähimaaston tarjoamista yrteistä sekä marjoista. Yrityksen suosio lähti nousukiitoon, kun tislaamon lanseeraama ”Napue Gin” voitti vuonna 2015 alan vanhimman sekä yhden merkittävimmistä ”International Wine and Spirits” -kilpailun ”Gin&Tonic” osion noin 150 kilpailevan ginin joukosta. Tällä hetkellä tislaamon tuotteita myydään jo lähes kolmessakymmenessä eri maassa ja suosio jatkaa laajenemistaan. Mikä on yrityksen salaisuus?
            </p>

            <p id="main">"Rukiinen maku tuo uutta täydennystä perinteikkäämpiinkin juomiin."
            </p>
			  
			<h2>TISLAAMON JUURI JA RAKENNE</h2>

            <p>Yritys sai alkunsa, kun suomalaiset kaverukset ihastuivat amerikkalaisen ruisviskin makuun ja saivat siitä samalla liikeidean yritykselleen. Tänä päivänä tislaamon tuotevalikoima koostuu gineistä, viskistä sekä long drinkistä. Yritys erottuu edukseen sillä, että sen kaikki tuotteet sisältävät ruista. Rukiista tislatut juomat voivat kuulostaa aluksi epäilyttäviltä, mutta niiden maku on saanut suosiota ympäri maailman juurikin sen vuoksi, että rukiinen maku tuo uutta täydennystä perinteikkäämpiinkin juomiin.
            </p>
			  
			

              <figure>
                <img id="image2" src="maailman2.jpg" alt="poisson">
                
              </figure>

           <p>Vuonna 1908 rakennettu tislaamorakennus tuottaa yritykselle lisäarvoa historiansa sekä yrityksen siellä tarjoamien lisäpalveluiden ansiosta. Tislaamo on alkuperältään meijerikiinteistö, joka toimi meijerinä vuoteen 2008 asti. Rakennusta on nyt käytetty Kyrö Distillery Companyn tislaamona jo neljän vuoden ajan ja sen yhteydessä toimii "Kyrön matkailun edistämiskeskus", joka on tislaamon oma vierailukeskus. Tislaamossa järjestetään myös erilaisia palveluita vierailijoille, kuten tislaamokierroksia, maistelua, dinner-paketteja, cocktailkoulutusta sekä workshoppeja oman ginin "luomiseen".  
            </p>

            <h2>SUOMALAINEN TUOTE, JOKA EI NÄYTÄ JUNTILTA</h2>

            <p>Kyrö Distillery Company on brändännyt tuotteensa laadun, kotimaisuutensa sekä paikallisuutensa mukaan, joka vaikuttaa toimivan niin Suomessa kuin myös ympäri maailmaa. Laadukkaat lähialueelta hankitut raaka-aineet vahvistavat tätä mielikuvaa. Yrityksen valmistamien tuotteiden sekä markkinoinnin ulkonäkö on suunniteltu laadukkaan näköisiksi aina some-markkinoinnista tuotteiden etiketteihin asti. Lisäksi myös itse tislaamon brändi poikkeaa massasta siten, että yritys on saanut työntekijänsä onnistuneesti osaksi tuotemerkkiään. Tämä näkyy muun muassa siinä miten yrityksen työntekijät on kuvailtu nettisivuilla: persoonallisuuden mukaisesti pilke silmäkulmassa. Lisäksi markkinointimateriaaleissa näkee usein työntekijät tuotteiden lisäksi, joka auttaa yritystä erottumaan edukseen sillä useimmiten yrityksissä henkilöstö ei ole niin suuresti näkyvillä, mitä Kyrö Distillery Companyssa on.</p>
			  
			<p>Brändi on alkujaankin luotu kansainväliseksi tuotteeksi tuotteiden ulkomuodon perusteella, mutta onko se ollut tarkoituksellista vai ei? Oli se tarkoituksellista tai ei, siinä on onnistuttu sillä yrityksen tunnettuus jatkaa kasvuaan ympäri maailmaa jatkuvasti. Kyrö Distillery Company on näkyvillä kuluttajan arjessa: kauppojen hyllyillä, tapahtumissa ja somessa. Tuotteiden ulkonäkö on suunniteltu miellyttämään kuluttajan silmää yksinkertaisten, laadukkaan näköisten etikettien avulla.</p>
			  
			<p>Saamastaan suosiosta ja menestyksestä huolimatta Kyrö Distillery Company vaikuttaa kuluttajamielessä helposti lähestyttävältä ja avoimelta yritykseltä. Yrityksen työntekijöitä ei siis ole niinkään tarkoituksella nostettu kuluttajakansan saavuttamattomiin vaan heidät tuodaan kuluttajien ”lähelle”.  Tällainen avoimuus, varsinkin suomalaisessa kulttuurissa, erottuu massasta ja näin parhaimmillaan saattaa auttaa luomaan aivan uudenlaisen suhteen yritykseen, yrityksen helposti lähestyttävien ja avointen työntekijöiden kautta.  </p>

