            <p>Nuorena luin ja myös piirsin paljon sarjakuvia. Batman on aina ollut suosikkini sarjakuvahahmoista ja hänellä on myös sarjakuvamaailman parhaimmat ja värikkäimmät roistot. Muistan elävästi kuin näin ensimmäisen kerran Batman Begins -elokuvan trailerin televisiosta. Yllätyin, että joku oli tehnyt uuden Batman -elokuvan ja koska asuin elokuvateatterin naapurissa, päätin katsastaa mielenkiintoiselta näyttävän leffan. Leffan jälkeen olin todella vaikuttunut elokuvan tarinanker  ronnasta ja siitä miten pelkoa käsiteltiin läpi elokuvan pääteemana päähenkilön ja roistojen kautta. En muista nähneeni näin kypsää ja älykästä supersankarileffaa aiemmin, vaikka myös Tim Burtonin Batman (1989) ja Bryan Singerin X-Men (2000) olivat tummasävyisiä ja enemmän aikuisille suunnattuja.
            </p>

            <p id="main">Millainen olisi tämän uuden realistisen lepakkouniversumin Jokeri?
            </p>

            <p>Lähtiessäni elokuvateatterista olin edelleen elokuvan lumoissa. Vahvana mielessä oli varsinkin sen loppukohtaus, jossa Batman tapaa komisario Gordonin katolla ja he keskustelevat uudesta rikollisesta, joka jättää rikospaikalle käyntikortin. Gordon ojentaa Batmanille todistepussin, jossa on pelikortti väärinpäin ja kun Batman kääntää kortin kuvapuolen esiin, sieltä paljastuu Jokeri! Viime hetkellä elokuvaan kuvattu loppukohtaus laittoi mielikuvituksen lentämään - millainen olisi tämän uuden realistisen lepakkouniversumin Jokeri?

              Elokuvan jälkeen aloin seurata ohjaaja Christopher Nolanin tekemisiä tarkemmin ja olin iloisesti yllättynyt, kun hän ilmoitti vuonna 2007 tekevänsä jatko-osaa ensimmäiseen Batman-elokuvaansa. Tuleva elokuva kiinnosti minua siinä määrin, että aloin seurata elokuvien uutissivuja ja foorumeita, kuten SuperHeroHype, Jow ja Batman On Film.
            </p>

            <h2>IT’S ALIVE… AND VIRAL!</h2>

            <p>
              Elokuvan fanifoorumit heräsivät henkiin huhtikuussa 2007, kun eräältä sivustolta löytyi käsikirjoitussivuja elokuvan roolitukseen liittyen ja yhdessä sivussa oli piiloviesti “Nähdään joulukuussa”. Virallinen sivu avautui toukokuussa 2007 ja siellä oli linkki Harvey Dentin vaalikampanjasivustolle. Pian fanifoorumille ilmestyi viestiä, että kalifornialaisesta sarjakuvaliikkeestä oli löytynyt jokeri-kortteja, joihin oli kirjoitettu ”I believe in Harvey Dent too! Hahahaha!”. Tämä vihje johti samanimiselle sivustolle, jossa ohjattiin ihmisiä antamaan sähköpostiosoitteensa. Monien tuntien jälkeen, kun tarpeeksi moni ihminen oli sähköpostiosoitteensa antanut, sivustolta paljastui ensimmäinen kuva Heath Ledgeristä Jokerina.
            </p>

            <h2>THE GAME IS ON</h2>

            <p>
              Kesällä 2007 foorumeille alkoi ilmestyä ensimmäisiä kuvia kuvauspaikoilta, kun Nolan kuvasi elokuvan alun 6-minuuttista prologia. Tästä riitti puhuttavaa kesän loppuun, kunnes heinäkuun lopussa fanit löysivät yhden dollarin Jokeri-seteleitä San Diegon sarjakuvafestareilta. Seuraavana päivänä taivaalle ilmestyi puhelinnumero. Peli oli alkanut. Lisäohjeissa neuvottiin pukeutumaan Jokeriksi ja suorittamaan tiettyjä tehtäviä kaupungilla. Paikan päällä osallistuneet saivat palkkioksi klovninaamioita ja netissä pelanneet ensimmäisen teaser-trailerin elokuvasta.
            </p>

            <h2>THE LONG HALLOWEEN</h2>

            <p>
              Lokakuun loppuun asti minä ja muut fanit kulutimme aikaa foorumeilla kuvauskuvia katsellen ja spekuloiden mm. leffan juonta, kunnes WhySoSerious-sivusto heräsi taas henkiin. Sivustolle ilmestyi hiljalleen mädäntyvä kurpitsa, joka viittasi tunnettuun Batman sarjakuvaan The Long Halloween. Kurpitsan mädännyttyä sivustolta paljastui 49 kuvalappusta, joissa oli vihjeitä valokuvien ottamiseen 49 kaupungissa. Kun kaikki oikeasta paikasta otetut kuvat saatiin suoritettua, sivustolta paljastui uusi Jokeri-kuva elokuvasta. Kuvaa klikkaamalla paljastui taas uusi sivusto, RorysDeathKiss.com, jossa oli Jokeri-näköiskilpailu. Kilpailijan piti kuvauttaa itsensä Jokerina ja lähettää se sivustolle. Tähän tuli myös osallistuttua monen muun nettipelaajan tavoin. Palkkioksi pelaajat saivat Gotham Times -sanomalehtiä postitse, jonka artikkeleista paljastui uusia vihjeitä ja nettisivuja. Nettisivuilta löytyneiden puhelinsoittojen ja salakuuntelu-äänitteiden kautta kilpailijoille aukesi uusi maailma Gothamin korruptoituneeseen poliisiin ja sen juonitteluihin mafian kanssa.
            </p>

