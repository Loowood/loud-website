            <h2>THE BEST LAUGH</h2>

            <p>
              Joulukuussa 2007 tapahtui koko kampanjan hauskin tempaus. WhySoSerious-sivuston vihjeiden kautta kilpailijoita kehotettiin hakemaan kakkuja salasanaa vastaan 22:sta leipomosta ympäri USA:ta. Kilpailijat hakivat kakun ja laittoivat sen jääkaappiin. Yllätys oli melkoinen, kun kakuista alkoi kuulua puhelimen pirinää! Kakun sisältä löytyi puhelin, jossa oli laturi ja ohjeet Jokerilta. Jokerin vihjeet ja ohjeet johtivat uuteen julisteeseen ja 6-minuuttisen prologin ensiesitykseen viidessä IMAX-elokuvateatterissa. Muiden sivustojen vihjeistä ja arvoituksista selvisi, että virallinen traileri julkaistaisiin vielä kuun lopussa.
              Tammikuussa 2008 Jokerin kakkupuhelimen löytäjät saivat tarkistusviestin pomoltaan. Siinä kehotettiin pysymään valppaana ja odottamaan lisäohjeita. Moni foorumin pääkäyttäjä ja ylläpitäjä oli Jokerin kakkusaajien listalla ja tämä oli hyvä keino pitää foorumit mukana pelissä.
            </p>

            <h2>I BELIEVE IN HARVEY DENT</h2>

            <p>Maaliskuussa 2008 Harvey Dentin vaalikampanjasivusto aktivoitui ja kilpailijoita kehotettiin rekisteröitymään kannattajiksi. Tämän jälkeen osa rekisteröityneistä sai kampanjamateriaalia, julisteiden, pinssien, t-paitojen ja tarrojen muodossa. Dentin sivuilta löytyi myös kartta ja Dent-mobiilin aikataulu, joka kiertäisi useammassa kaupungissa pitämässä vaalitilaisuuksia. Ohikulkijoiden ja muiden tietämättömien kummastukseksi osa vaalikampanjoista oli melko isoja ja aiheutti varmasti ihmetystä.
            Huhtikuussa Jokeri-puhelinten omistajat saivat uusia ohjeita, joissa kehotettiin etsimään tiettyjä keilapalloja keilaradoilta ympäri maailmaa. Näiden tehtävien ratkaisijat löysivät keilapalloja ja puhelimia, joissa oli uusia vihjeitä. Harvey Dentin kampanjan painopiste siirtyi Gothamin korruptoituneeseen poliisiin ja myös Citizens for Batman -kampanja aloitti toimintansa. Tähän kampanjaan osallistuneiden piti mm. ottaa kuva Batmanista ja lähettää se sivustolle. Minä ja kaverini Jyväskylästä osallistuttiin tähän ja ystäväni sai postitse palkkioksi mm. Batman-avaimenperän ja Gotham Times -sanomalehden. Valitettavasti minä en saanut mitään. Lohdutukseksi sain toki puhelinsoiton eräänä lauantaiaamuna komisario Gordonilta eli Gary Oldmanilta, joka pyysi apuani tulevaan ratsiaan, koska olin osallistunut netissä poliisien ja järjestäytyneen rikollisuuden tutkintaan. Kyseessä oli valmiiksi nauhoitettu ääniviesti, mutta olin iloisesti yllättynyt tulevan Oscar-voittajan soitosta. Tämä tehtävä johti 30 korruptoituneen poliisin pidätykseen ja siitä palkkioksi kilpailijat saivat uuden julisteen ja elokuvatrailerin, jota näytettiin 12 kaupungissa ja myöhemmin myös nettikilpailijat pystyivät näkemään saman trailerin latauslinkin kautta.
            </p>

            <h2>VIRTUAL WORLD OF GOTHAM</h2>

            <p>Elokuvan ensi-ilta oli heinäkuussa 2008 ja sitä edeltänyt kuukausi oli täynnä tehtäviä. Uusia arvoituksia ja tehtäviä oli mm. pizza-laatikoissa ja Gotham Times aloitti uutislähetykset. Joka viikko aina ensi-iltaan asti tällä sivustolla esitettiin uusi jakso uutislähetystä, jossa käsiteltiin viraalipelin/elokuvan tapahtumia yms. Viimeisinä viikkoina ennen ensi-iltaa puhelinten omistajat saivat ohjeita ja vihjeitä viimeisiin tehtäviin. Why So Serious -sivusto julkaisi tehtävälistan, josta näkyi kaikki suoritetut ja suorittamattomat tehtävät ja niiden nettisivut. Viimeisten viikkojen virtuaalipelin ja maailman tapahtumat nivoutuivat melkein saumattomasti elokuvan alkukohtaukseen eli pankkiryöstöön. Näin ollen kilpailijat olivat eläneet elokuvan virtuaalimaailmassa saman ajan jakson, jonka Batman eli elokuvan ensimmäisestä osan lopusta jatko-osan alkuun.
            </p>

            <h2>THE MOST INNOVATIVE VIRAL CAMPAIGN EVER</h2>

            <p>Yön Ritari -elokuvan viraalikampanja on jäänyt mieleen innovatiivisimpana markkinointikampanjana, jonka olen nähnyt ja kokenut tähän päivään mennessä. Pelin 15 kuukauden aikana tuli koettua ikimuistoisia hetkiä sekä vietettyä satoja tunteja illasta aamuyöhön foorumeilla muiden kisailijoiden kanssa, samalla kun Amerikan kollegat juoksivat pitkin oikeita katuja suurkaupungeissa. Mukaan mahtui paljon valvottuja öitä, koska suurin osa tehtävistä tapahtui USA aikavyöhykkeellä. Elokuvan ilmestymisen jälkeen sain vihdoin palkintoni, kun sähköpostiin ilmestyi latauslinkki elokuvan mosaiikkijulisteeseen, joka oli koottu kampanjan aikana lähetetyistä Jokeri-näköiskuvista. Löysin muutaman minuutin tutkimisen jälkeen itseni kuvasta, joten se parhaiten nauraa, joka viimeksi nauraa… ha Ha HA.

