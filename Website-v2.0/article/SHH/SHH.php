            <p>Kuuntele. Mitä kaikkea ympärilläsi tapahtuu? Kuuletko puheen sorinaa, liikenteen melua tai musiikkia? Vai istutko kenties paikassa, joka on täysin hiljainen? Jos kuuntelet vielä tarkemmin, todennäköisesti ilmastointilaite suhisee tai kuulet jotain muuta vaimeampaa ääntä.
            </p>
            <p>
              Äänet ympäröivät meidät, ne ovat osa meitä, ne valtaavat tilat joissa olemme. Tuotamme myös itse ääniä jatkuvasti, puhumme, laulamme, hyräilemme, soitamme, kuiskaamme ja metelöimme. Elämisestä kuuluu jatkuvasti ääniä ja olemme osa niitä. Kaupungin kupeessa saattaa jo olla sen verran hiljaisempaa, että se sallii jopa nautittavimpien äänten kuten lintujen laulun kuulumisen. Siirtyessä urbaanista ympäristöstä kokonaan pois, esimerkiksi metsään, mieleen tulee ensimmäisenä rauhoittuminen. Rauhoittuminen ilman melua hiljaisuudessa tai ainakin hiljaisemmassa paikassa missä yleensä viettää aikaansa. Luultavasti polulla kävellessäsi hiekka rapisee jalkojesi alla ja katkot vahingossa muutaman oksan. Syvemmällä metsässä kosteammilla alueilla sammal litisee. Linnut laulavat ja saattavat jopa huutaa, jos pelästytät ne. Tuuli humisee heilutellen puiden oksia ja sadepisarat tipahtelevat lehdiltä maahan. Ukkonen kuulostaa siltä kuin taivas repeäisi. Luontokaan ei ole hiljainen.
            </p>
            <h2>Melusaaste vaikuttaa terveyteemme</h2>
            <p>Olemme tottuneet koviin ääniin ja jatkuvaan meteliin arjessamme ja hyväksymme sen. Melusaaste vaikuttaa niin ihmisiin kuin eläimiinkin. Koulussa sekä työpaikalla saattavat yleinen hälinä ja kovat äänet vaikeuttaa keskittymistä ja haitata todella paljon tehtävistä suoriutumista. Öinen meteli aiheuttaa usein ongelmia unen kanssa, jolloin se vaikuttaa yleiseen jaksamiseen ja lisää stressiä. Liiallinen stressi nostattaa verenpainetta ja altistaa sydän- ja verisuonitaudeille, jotka puolestaan lyhentävät eliniänodotetta. Jatkuvan melun sietäminen on myös henkisesti raskasta. Jokaisella ihmisellä on omat rajansa ja toisinaan sietokyky ylittyy desibelien noustessa vaarallisille tasoille. Kouluympäristössä tämän kaltaisiin tilanteisiin vastataan valitettavan usein niin, että joku karjaisee entistä kovempaa saadakseen muiden huomion ja pyytää sen jälkeen hiljaisuutta.
            </p>
            <p id="main">“Luonnon rauhasta tulee arjen luksusta”
            </p>
            <p>
              Melusaasteiden terveysvaikutuksien vuoksi osa japanilaisista lääkäreistä määrää potilaitaan metsään.  Metsäterapia toimii stressin hoitokeinona, koska metsien hiljaisuus ja rauha helpottavat masennusta, ärtyneisyyttä ja ahdistusta. Metsä on tuotteistettu ja luonnon rauhasta tulee arjen luksusta. Metsäterapian tarve kuvastaa hyvin nykyajan ihmisten ihannoimaa hektistä elämäntyyliä, jossa rauhan arvostus saattaa muodostua nousevaksi trendiksi. Tietyissä ympäristöissä hiljaisuus on edelleen normi, sillä kaupungin kirjastoissa sekä useissa gallerioissa ihmiset osaavat olla hiljaa ja puhua toisilleen rauhallisesti muita häiritsemättä. Miksei sama päde kouluihin ja työpaikkoihin, joissa keskittymisen ja rauhan pitäisi olla vähintäänkin samaa luokkaa?
            </p>
            <h2>Hiljaa hyvä tulee</h2>
            <p>Oikeuttaako arjen melun hyväksyminen sen, että saamme myös pitää meteliä missä ja milloin haluamme tilanteista huolimatta? Käsitteemme hiljaisuudesta on varmasti muuttunut. Osalle se tarkoittaa taustamusiikin kääntämistä hiljaisemmalle, yleisen puheen sorinan puuttumista, osalle luonnon hiljaisuutta ja ehkä on vielä ihmisiä jotka kaipaavat täydellistä hiljaisuutta. Muutoksen voi nähdä parhaiten vanhemmistamme omasta iästämme huolimatta.
              Melutottumukset muuttuvat samaan tahtiin teknologian kehityksen kanssa esimerkiksi suurista teollisuuteen tarkoitetuista koneista pyritään nykyisin tekemään yhä hiljaisempia, kun taas pienistä matkasoittimista tehdään tehokkaampia ja äänen voimakkuus sekä laatu pyritään maksimoimaan. Sähköautotkin ovat jo niin hiljaisia, että auton lähestymistä on vaikea havaita pelkän äänen avulla. Naapurin vanhus valittaa seinän läpi kuuluvasta melusta, vaikka kärsii luultavasti kuulonalenemasta vanhojen tehdasaikojen takia ja tuntee pelkän basson jytkeen tai sitten musiikin soittajalla on kyseinen vaiva omien tottumuksiensa vuoksi ja siksi musiikki ylipäätään soi liian kovaa.
            </p>
            <p>
              Näkemyseroista huolimatta pitäisi kunnioituksen toisiamme kohtaan olla vielä tallella. On kohteliasta ja vähintäänkin toivottavaa pyrkiä antamaan varsinkin samassa tilassa työskenteleville se työrauha jonka he tarvitsevat.
              Jos itse pystyt työskentelemään paremmin musiikkia
              kuunnellen, voit laittaa kuulokkeet korville. Yleensä sinun on myös mahdollisuus siirtyä eri tiloihin rauhoittumaan tai vastavuoroisesti puhumaan ystävillesi asioista, jotka eivät siihen tilanteeseen tai tilaan liity. Hiljaisuuttakin kannattaa kuunnella.
            </p>
